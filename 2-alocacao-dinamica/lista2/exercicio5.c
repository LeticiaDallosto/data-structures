// Suponha que os elementos do vetor v são do tipo int e cada int ocupa 8 bytes no seu computador.
// Se o endereço de v[0] é 55000, qual o valor da expressão  v + 3?

/* 

O endereco de v[3] será:
endereco de v[3] = endereco de v[0] + 3 * tamanho de cada elemento
endereco de v[3] = 55000 + (3 * 8)
endereco de v[3] = 55000 + 24
endereco de v[3] = 55024
Portanto, o valor da expressao v + 3 seria 55024.

*/






